function plotPolynomialRegression(varargin)
  terms = [ ...
    0, 0; ...
    1, 0; ...
    0, 1; ...
    1, 1; ...
    2, 0; ...
    2, 1; ...
  ];

  assess('leakageOptions', Options( ...
    'approximation', 'Regression.LogPolynomial', ...
    'terms', terms, ...
    'countConstraints', Options('T', 50, 'Leff', 50), ...
    'rangeConstraints', Options('T', Utils.toKelvin([ 40, 400 ]))), ...
    varargin{:});
end
