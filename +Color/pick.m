function color = pick(i)
  if nargin == 0, i = randi(100); end

  palette = { ...
    [ 87, 181, 232] / 255, ...
    [230, 158,   0] / 255, ...
    [129, 197, 122] / 255, ...
    [20,   43, 140] / 255, ...
    [195,   0, 191] / 255, ...
    [112, 128, 142] / 255, ...
    [143,  25,  31] / 255 ...
  };

  color = palette{mod(i - 1, length(palette)) + 1};
end
